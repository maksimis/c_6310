// Подключаем стандартную библиотеку ввода-вывода для использования printf
#include <stdio.h>

// Главная функция программы, с неё начинается выполнение
int main() {
    // Пример работы с типом char* (указатель на символ) - строки в языке C
    const char* msg[] = {"Hello", "C", "World", "from", "VS Code", "and the C extension!"};
    
    // Вычисляем размер массива: общий размер массива делится на размер одного элемента
    int size = sizeof(msg) / sizeof(msg[0]);
    
    // Цикл for для перебора всех элементов массива
    // int i = 0 - инициализация счётчика (тип int - целое число)
    for (int i = 0; i < size; i++) {
        // printf выводит строку: %s - спецификатор формата для строки
        printf("%s ", msg[i]);
    }
    
    // Перенос строки после вывода всех слов
    printf("\n");
    
    // ДЕМОНСТРАЦИЯ БАЗОВЫХ ТИПОВ ДАННЫХ
    // 1. int - целое число
    int age = 25;
    printf("Целое число (int): Возраст = %d лет\n", age);
    
    // 2. float - число с плавающей точкой
    float temperature = 23.5f;
    printf("Дробное число (float): Температура = %.1f градусов\n", temperature);
    
    // 3. char - одиночный символ
    char grade = 'A';
    printf("Символ (char): Оценка = %c\n", grade);
    
    // 4. Пример арифметики с разными типами
    int students = 30;
    float average_score = 4.25f;
    
    printf("Количество студентов (int): %d\n", students);
    printf("Средний балл (float): %.2f\n", average_score);
    
    // 5. Пример преобразования типов
    float total_score = students * average_score;
    printf("Общая сумма баллов (float): %.2f\n", total_score);
    
    // 6. Массив символов (строка в C)
    char name[] = "Иван";
    printf("Массив символов (строка): Имя = %s\n", name);
    
    // Возвращаем 0 - признак успешного завершения программы
    return 0;
}
